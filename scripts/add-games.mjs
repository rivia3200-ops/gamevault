function cg(slug) { return `https://www.crazygames.com/embed/${slug}`; }
function thumb(bg, fg, text) { return `https://placehold.co/270x180/${bg}/${fg}?text=${encodeURIComponent(text)}&font=raleway`; }
function banner(bg, fg, text) { return `https://placehold.co/800x450/${bg}/${fg}?text=${encodeURIComponent(text)}&font=raleway`; }

