writeFileSync(gamesPath, JSON.stringify(merged, null, 2));

// Update category counts
const counts = {};
for (const game of merged) {
  counts[game.category] = (counts[game.category] || 0) + 1;
}
const categories = JSON.parse(readFileSync(categoriesPath, 'utf8'));
for (const cat of categories) {
  if (counts[cat.slug] !== undefined) cat.gameCount = counts[cat.slug];
}
writeFileSync(categoriesPath, JSON.stringify(categories, null, 2));

