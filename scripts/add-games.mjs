function g(id, title, slug, category, embedUrl, plays, rating, ratingCount, opts = {}) {
  const [bg, fg] = COLORS[category] || ['1a1a1a','ffffff'];
  return {
    id, title, slug,
    description: opts.desc || `Play ${title} online for free in your browser. No download needed.`,
    shortDescription: opts.short || `Play ${title} free in your browser.`,
    category,
    tags: opts.tags || [],
    thumbnailUrl: opts.thumb || thumb(bg, fg, title),
    bannerUrl: opts.banner || banner(bg, fg, title),
    embedUrl,
    embedType: 'iframe',
    width: opts.w || 960,
    height: opts.h || 540,
    developer: opts.dev || 'CrazyGames',
    plays, rating, ratingCount,
    featured: opts.featured || false,
    isNew: opts.isNew || false,
    isHot: opts.isHot || false,
    addedAt: opts.addedAt || '2024-01-01',
    instructions: opts.instr || 'Follow on-screen instructions.',
    controls: opts.ctrl || { keyboard: 'WASD or Arrow Keys', mouse: 'Click to interact' },
  };
}

